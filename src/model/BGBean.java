package model;

public class BGBean {
	int r,b,g;
	public BGBean(){
		r=0;
		g=255;
		b=0;
	}
	public int getR() {
		return r;
	}
	public void setR(int r) {
		this.r = r;
	}
	public int getB() {
		return b;
	}
	public void setB(int b) {
		this.b = b;
	}
	public int getG() {
		return g;
	}
	public void setG(int g) {
		this.g = g;
	}
	

}
